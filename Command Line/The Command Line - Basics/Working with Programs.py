## Setting Variables

# While there are many UNIX shells, Bash is one of the most popular.
# Bash is the default shell on most Linux and OS X computers

OS=linux
OPERATING_SYSTEM="linux"

# Both of the variables above, OS and OPERATING_SYSTEM, will actually end up with the same value.
# That's because quotes are optional when using strings in Bash, unless the string contains a space.
# Bash is sensitive to spaces, so strings that have them won't work properly if we don't surround them with quotes



